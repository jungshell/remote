def add_calc_sheet(wb, title, data_rows):
    title = title[:31]
    ws = wb.create_sheet(title)
    headers = [
        "번호", "점검 항목", "기준일 입력 안내", "기준일\n(직접 입력)",
        "가산일수\n(+마감/-선행)", "가산개월\n(0이면 일수만)",
        "마감·기한일\n(자동)", "비고", "근거",
    ]
    ws.append(headers)
    for c in range(1, len(headers) + 1):
        cell = ws.cell(1, c)
        cell.fill = HEADER_FILL
        cell.font = HEADER_FONT
        cell.border = thin_border()
        cell.alignment = Alignment(wrap_text=True, vertical="center")

    for i, (name, guide, d_off, m_off, note, cite) in enumerate(data_rows, start=1):
        row = ws.max_row + 1
        ws.cell(row, 1, i)
        ws.cell(row, 2, name)
        ws.cell(row, 3, guide)
        ws.cell(row, 4, None)
        ws.cell(row, 5, d_off)
        ws.cell(row, 6, m_off)
        col_d = get_column_letter(4)
        col_e = get_column_letter(5)
        col_f = get_column_letter(6)
        ws.cell(row, 7).value = (
            f'=IF({col_d}{row}="","",IF({col_f}{row}<>0,EDATE({col_d}{row},{col_f}{row}),{col_d}{row}+{col_e}{row}))'
        )
        ws.cell(row, 8, note or "")
        ws.cell(row, 9, cite)
        for c in range(1, 10):
            cell = ws.cell(row, c)
            cell.border = thin_border()
            cell.alignment = Alignment(wrap_text=True, vertical="top")
            if c == 4:
                cell.fill = INPUT_FILL
                cell.number_format = "yyyy-mm-dd"
            if c == 7:
                cell.number_format = "yyyy-mm-dd"
    ws.freeze_panes = "A2"
    auto_width(ws)


