      console.log('✅ 토큰 복구 성공! 길이:', token.length);
      // 복구된 토큰을 다시 모든 저장소에 저장
      localStorage.setItem('token', token);
      localStorage.setItem('auth_token_backup', token);
      sessionStorage.setItem('token', token);
