    if (!token || !user) {
      console.log('❌ 인증 실패 - 토큰:', !!token, '사용자:', !!user);
      toast({
        title: '투표 실패',
        description: '로그인이 필요합니다. 로그인 페이지로 이동합니다.',
        status: 'error',
        duration: 3000,
        isClosable: true,
      });
      // 로그인 페이지로 리다이렉트
      navigate('/login');
      return;
    }

